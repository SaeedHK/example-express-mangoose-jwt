const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
  // unique Ids are generated automotically by mongoose
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('User', UserSchema);
